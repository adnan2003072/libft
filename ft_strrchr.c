#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	if (!s)
		return (0);
	i = ft_strlen(s);
	while (s[i] && s[i] != c)
		i--;
	return ((char *) s + i);
}
