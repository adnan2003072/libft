#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i] && s2[i] && i < n)
	{
		if ((t_uchar)s1[i] != (t_uchar)s2[i])
			return ((t_uchar)s1[i] - (t_uchar)s2[i]);
		i++;
	}
	return (0);
}
