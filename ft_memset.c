#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	t_uchar	*s1;
	size_t	i;

	if (!s)
		return (0);
	s1 = s;
	i = 0;
	while (i < n)
	{
		s1[i] = c;
		i++;
	}
	return (s);
}
